// Cache-busting bundle step (non-negotiable #4): every dispatcher / customer tab
// must pick up CSS changes after a deploy. We hash the compiled CSS into its
// filename and rewrite every HTML <link> to point at the hashed file, so a
// changed stylesheet is a changed URL the browser cannot serve stale.
//
// Output: ./dist is a ready-to-FTP folder (HTML + hashed CSS + assets + .htaccess).
import {
  readFileSync,
  writeFileSync,
  readdirSync,
  cpSync,
  rmSync,
} from "node:fs";
import { createHash } from "node:crypto";
import { join } from "node:path";
import { expandIncludes } from "./includes.mjs";

const DIST = "dist";
const SITE = "site";
const PARTIALS = join(SITE, "partials");
const PLAIN_CSS = join(DIST, "styles.css");

// 1. Content hash of the compiled CSS.
const css = readFileSync(PLAIN_CSS);
const hash = createHash("sha256").update(css).digest("hex").slice(0, 12);
const hashedName = `styles.${hash}.css`;
writeFileSync(join(DIST, hashedName), css);

// 2. Copy authored site (HTML + assets) into dist, overwriting prior output.
cpSync(SITE, DIST, { recursive: true });

// 3. Inline shared partials (header/footer — DRY, authored once in
//    site/partials/) then rewrite stylesheet refs to the hashed filename.
//    The CSS regex matches the dev placeholder `styles.css` and any previous
//    `styles.<hash>.css`.
const linkRe = /href="(?:\.\/)?styles(?:\.[0-9a-f]{12})?\.css"/g;
const htmlFiles = readdirSync(DIST).filter((f) => f.endsWith(".html"));
for (const f of htmlFiles) {
  const p = join(DIST, f);
  const html = expandIncludes(readFileSync(p, "utf8"), PARTIALS);
  const next = html.replace(linkRe, `href="${hashedName}"`);
  if (next === html) {
    console.warn(`  ! ${f}: no <link href="styles.css"> found to rewrite`);
  }
  writeFileSync(p, next);
}

// The partials are a build-time source, not a shipped page — drop the copy that
// cpSync placed in dist/.
rmSync(join(DIST, "partials"), { recursive: true, force: true });

// 4. Belt-and-suspenders cache policy for Apache/FTP hosting:
//    hashed assets cached forever, HTML always revalidated.
const htaccess = `# Generated by scripts/bundle.mjs — do not edit by hand.
ErrorDocument 404 /404.html
<IfModule mod_headers.c>
  # Immutable, content-hashed assets: cache aggressively.
  <FilesMatch "\\.(css|js|woff2?|png|jpe?g|svg|webp)$">
    Header set Cache-Control "public, max-age=31536000, immutable"
  </FilesMatch>
  # HTML must always be revalidated so new hashed CSS is picked up immediately.
  <FilesMatch "\\.html$">
    Header set Cache-Control "no-cache, must-revalidate"
  </FilesMatch>
</IfModule>
`;
writeFileSync(join(DIST, ".htaccess"), htaccess);

// 5. Drop the un-hashed CSS so exactly one compiled stylesheet ships (#7).
rmSync(PLAIN_CSS, { force: true });

console.log(`  ✓ Bundled: dist/${hashedName} + ${htmlFiles.length} HTML page(s) rewritten, .htaccess written`);
